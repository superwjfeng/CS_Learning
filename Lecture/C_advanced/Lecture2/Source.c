int main()
{
	int n = 9;
	//00000000000000000000000000001001
	float* pFloat = (float*)&n;
	printf("%d\n", n); //9
	printf("%f\n", *pFloat); //0.000000
	

	*pFloat = 9.0;
	//9.0D = 1001.0B = 1.001 * 2^3
	//(-1)^0 * 1.001 * 2^3
	//S = 0; M = 1.001; E = 3+1227=130;
	//0 10000010 00100000000000000000000 = 1091567616
	printf("%d\n", n); //1091567616
	printf("%f\n", *pFloat); //9.000000
	//正数和浮点数在内存中的存储方式是有差异的
	return 0;
}